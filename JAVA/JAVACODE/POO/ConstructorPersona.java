package POO;

public class ConstructorPersona {
    private String nombre;
    private String apellido;
    private int edad;
    private double altura;

    public ConstructorPersona(String nombre, String apellido, int edad, double altura) {
        this.nombre = nombre;
        this.apellido = apellido;
        this.edad = edad;
        this.altura = altura;

    }
}


